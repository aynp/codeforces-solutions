#define int int64_t
typedef long long ll;
typedef long double ld;
using namespace std;

void solve(){
    int r,c;
    std::cin >> r >> c;
    char a[r][c];
    int rc=0,cc=0;
    std::vector<bool> row(r,true),clmn(c,true);

    for (int i = 0; i < r; i++){
        for (int j = 0; j < c; j++){
            
            std::cin >> a[i][j];
            
            if(a[i][j] == 'S'){
                if(row[i]){
                    rc++;
                    row[i] = false;
                }
                if(clmn[j]){
                    cc++;
                    clmn[j] = false;
                }
            }

        }
    }
    // std::cout << rc << " " << cc << '\n';
    std::cout << max(r*c-rc*cc,(int64_t)0) << '\n';
}

int32_t main(){
    std::ios::sync_with_stdio(false);
    std::cin.tie(NULL);

    solve();

    return 0;
}//main
