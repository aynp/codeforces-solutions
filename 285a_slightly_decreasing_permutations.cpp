typedef long double ld;
using namespace std;

void solve(){
    int n, a;
    std::cin >> n >> a;
    std::vector<int> v(n);
    for (int i = 0; i+a < n; i++){
        v[i] = i+a+1;
    }
    for (int i = 0; i < a; i++){
        v[n-a+i] = a-i;
    }
    
    for (int i = 0; i < n; i++){
        std::cout << v[i] << ' ';
    }
}

int32_t main(){
    std::ios::sync_with_stdio(false);
    std::cin.tie(NULL);

    solve();

    return 0;
}//main
