#include <bits/stdc++.h>
#define MOD 1'000'000'007
//#define MOD = 998'244'353
#define int int64_t
typedef long long ll;
typedef long double ld;
using namespace std;

void solve(){
	int n, t = 0;
    cin >> n;
    int x[n], y[n];

    for (int i = 0; i < n; i++)
        cin >> x[i] >> y[i];

    for (int i = 0; i < n; i++)
    {
        bool r(false), l(false), u(false), d(false);
        for (int j = 0; j < n; j++)
        {
            if (x[j] > x[i] && y[j] == y[i]) r = true;
            if (x[j] < x[i] && y[j] == y[i]) l = true;
            if (x[j] == x[i] && y[j] > y[i]) u = true;
            if (x[j] == x[i] && y[j] < y[i]) d = true;
        }
        if (r && l && u && d) t++;
    }
    cout << t << endl;
    return;
}

int32_t main(){
	std::ios::sync_with_stdio(false);
	std::cin.tie(NULL);

	solve();

	return 0;
}//main
